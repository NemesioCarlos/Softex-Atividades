// considerando uma fila essa seria a sequência de números removidos: [3, 7, 9, 1, 0] usando .pop
let list = [3, 7, 9, 1, 0]
while ( list.length > 0) { 
  list.pop()
}
console.log(list)



// considerando uma pilha essa seria a sequência de números removidos: [0, 1, 9, 7, 3] usando .shift()
let list = [3, 7, 9, 1, 0]
while ( list.length > 0 ) {
list.shift()
}
console.log(list)